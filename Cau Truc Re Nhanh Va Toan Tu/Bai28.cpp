#define ll long long
#define FORN(i, a, b) for (int i = a; i < b; i++)
#define FORL(i, a, b) for (int i = a; i > b; i--)
#define FORNB(i, a, b) for (int i = a; i <= b; i++)
#define FORLB(i, a, b) for (int i = a; i >= b; i--)

int main()
{
    int n, u1, d;
    cin >> n >> u1 >> d;
    cout << 1ll * n * (2 * u1 + 1ll * (n - 1) * d) / 2;
    return 0;
}